//一个循环内，用%2取余数实现判定

#include <stdio.h>

int main() {
	int i, flag = 0;
	for (i = 77; i <= 210; i++)
		if (i % 2 == 0)
			flag++;
	printf("\n 77~210偶数有：%d个\n", flag);
	return 0;
}
