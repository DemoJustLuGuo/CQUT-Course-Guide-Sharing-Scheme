//一个循环实现取余数判定
//设定一个计数变量，记录不能被5整除的数的个数，满5个就输出一个换行符号
#include <stdio.h>

int main() {
	int i, flag = 0;
	printf("\n");
	for (i = 7; i <= 100; i++) {
		if (i % 5 != 0) {
			printf("%8d ", i);
			flag++;
			if (flag % 5 == 0)
				printf("\n");
		}
	}
	return 0;
}

