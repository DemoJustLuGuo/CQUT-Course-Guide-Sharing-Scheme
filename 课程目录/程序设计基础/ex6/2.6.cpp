//用一个循环实现
//每次循环首先找出该数个、十、百位上的数值，再用水仙花数条件判定

#include <stdio.h>

int main() {
	int i, a, b, c;
	printf("\n100~999中的水仙花数：\n");
	for (i = 100; i <= 999; i++) {
		a = i % 10;
		b = (i / 10) % 10;
		c = i / 100;
		if (a * a * a + b * b * b + c * c * c == i)
			printf("%d ", i);
	}
	printf("\n");
	return 0;
}
