//利用n!*(n+1)=(n+1)!

#include <stdio.h>

int main() {
	int i, sum = 1, k = 1, sign = 1;
	for (i = 2; i <= 6; i++) {
		k *= i;
		sign = -sign;
		sum += sign * k;
	}
	printf("\n1!-2!+3!-4!+5!-6!=%d\n", sum);
	return 0;
}
