//用2个嵌套循环，只输出1~9行j>=i的列，中间用制表符控制间隔
//j<i数组制表符，j>=i输出i*j

#include <stdio.h>

int main() {
	int i, j;
	printf("\n");
	for (i = 1; i <= 9; i++)
		for (j = 1; j <= 9; j++) {
			if (j < i)
				printf("\t");
			else
				printf("%d*%d=%d\t", i, j, i * j);
			if (j == 9)
				printf("\n");
		}

	return 0;
}
