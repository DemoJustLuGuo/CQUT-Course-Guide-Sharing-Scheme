//从键盘输入一个数a
//一个循环处理该数对1~a的数k取余是否为零，为零即要输出的因子

#include <stdio.h>

int main() {
	int a, k;
	scanf("%d", &a);
	if (a > 0) {
		printf("%d:", a);
		for (k = 1; k <= a; k++)
			if (a % k == 0)
				printf("%d ", k);
	}
	printf("\n");
}
